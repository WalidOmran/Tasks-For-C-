#include <iostream>
using namespace std;

int main(){

    short unsigned A , B;

    cout << "Please enter the first number A ? " << endl;
    cin  >> A;
    cout << "Please enter the second number B ?" << endl;
    cin  >> B;


    cout << "A + B = " << A + B << endl;
    cout << "A - B = " << A - B << endl;
    cout << "A * B = " << A * B << endl;
    cout << "A / B = " << A / B << endl;
    cout << "A % B = " << A % B << endl;
}